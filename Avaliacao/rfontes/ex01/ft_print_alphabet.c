#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	a;

	a = 'a';
	while (a <= 'z')
	{
		write(1, &a, 1);
		a = a + 1;
	}
}

int main(void)
{
	
	ft_print_alphabet();
}