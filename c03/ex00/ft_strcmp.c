int	ft_strcmp(char *s1, char *s2)
{
	int				i;
	unsigned char	s1x;
	unsigned char	s2x;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != 0 && s2[i] != 0)
	{
		i++;
	}
	s1x = s1[i];
	s2x = s2[i];
	return (s1x - s2x);
}
